        public static void PrintByteArray(byte[] dat)
        {
            foreach (byte b in dat)
            {
                Console.Write(((b.ToString("X").Length < 2) ? "0" : "") + b.ToString("X") + ' ');
            }
            Console.WriteLine();
        }

