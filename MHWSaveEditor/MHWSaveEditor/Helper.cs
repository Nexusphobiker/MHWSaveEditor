    public static class MemoryStreamExtension
    {
        private static byte[] uintBuff = new byte[4];
        public static uint ReadUInt(this System.IO.MemoryStream stream)
        {
            stream.Read(uintBuff, 0, uintBuff.Length);
            return BitConverter.ToUInt32(uintBuff, 0);
        }
    }
